// Arithmetic binary = "+", "-", "*", "/", "%"
public class Operators_02 {
    public static void main(String[] args) {
        int a = 23;
        int b = 23;

        int sum = a + b;
        int sub = a - b;
        int multi = a * b;
        int devide = a / b;
        int rem = a % b;

        System.out.println(sum);
        System.out.println(sub);
        System.out.println(multi);
        System.out.println(devide);
        System.out.println(rem);
    }
}
