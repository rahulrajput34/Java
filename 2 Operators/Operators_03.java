//Arithmetic unary = ++, --
public class Operators_03 {
    public static void main(String[] args) {
        int x = 23;

        int y = x++;
        System.out.println(x);
        System.out.println(y);

        int z = ++x;
        System.out.println(x);
        System.out.println(z);
    }


}

