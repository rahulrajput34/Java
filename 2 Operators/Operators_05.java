// Logical Operators
// we know about and or and not
public class Operators_05 {
    public static void main(String[] args) {
        int a = 23;
        int b = 20;
        System.out.println( a == b && a > b);
        System.out.println( a == b || a > b);
        System.out.println(!(a == b && a > b));
        
    }
}
