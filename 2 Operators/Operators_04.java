// Relational Operators = 
// "==", "!=", "<", ">", "<=", ">="

public class Operators_04 {
    public static void main(String[] args) {
        int a = 20;
        int b = 20;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a <= b);
        System.out.println(a >= b);
    }
}
