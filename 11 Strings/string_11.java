// convert all types to string
public class string_11 {

    public static void main(String[] args) {
        int a = 10; // we can not convert to string
        Integer b = 10; // we can convert to string
        b.toString();
        System.out.println(b);

        char a1 = 'a'; // we can not convert to string
        Character b1 = 'a'; // we can convert to string
        b.toString();
        System.out.println(b1);

    }
}