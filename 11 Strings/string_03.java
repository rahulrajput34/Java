// TO find a lenght in Sting we user String function inbuilt property.
// for string concatination , we use +
public class string_03 {
    public static void main(String[] args) {
        String rk = "rahul is a good boy";
        String rk2 = "rahul is boy";
        // string lenght
        System.out.println(rk.length());
        // string concationation
        System.out.println(rk + " " + rk2);
    }
}
